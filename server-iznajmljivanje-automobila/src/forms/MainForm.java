public void popuniTabelu() {
    tblKorisnici.setModel(new javax.swing.table.DefaultTableModel(
            new Object[][]{},
            new String[]{
                "Korisnicko ime"
            }
    ));

    List<AbstractDomainObject> prijavljeniKorisnici = Controller.getInstance().getPrijavljeniKorisnici();
    DefaultTableModel dtm = (DefaultTableModel) tblKorisnici.getModel();

    for (AbstractDomainObject ado : prijavljeniKorisnici) {
        if(ado instanceof Korisnik korisnik) {
            String[] row = { korisnik.getKorisnickoIme() };
            dtm.addRow(row);
        }
    }
}

